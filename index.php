      <h2 id="concept">Concept</h2>

      <div class="row">
        <div class="span4">
         <h3>Game Jam ?</h3>

         <p>Prenez des enthousiastes de la création de jeux vidéo, qu'ils soient amateurs, étudiants, professionnels ou tout simplement curieux, mélanger le tout pendant 24h à 72h en formant des équipes et vous obtenez des jeux vidéo innovants créés de A à Z sur place. Le tout dans une ambiance conviviale.<br/>
          Une game jam n'est pas une compétition, mais plutôt un moment de partage, d'apprentissage, d'innovation, de challenge personnel et de franche camaraderie.</p>
        </div>

        <div class="span4">
          <h3>BBQ Game Jam ?</h3>

          <p>La BBQ Game Jam est légèrement différente. Elle se déroule sur 24h (de 14h à 14h), avec des équipes de 1 à 5 personnes et le but est de créer un jeu sur un thème imposé, le tout autour d'un BBQ.
          </p>
        </div>

        <div class="span4">
          <h3>Au programme</h3>

          <p>À partir de 14h le samedi, tu viens avec ton équipement (ordinateur, tablette graphique, clavier midi, etc.), tu formes une équipe (ou tu as déjà la tienne), le thème est annoncé et c'est parti. À 19h, nous faisons un break pour papoter autour d'un barbecue.<br/>
            À 14h le lendemain, chaque jeu est présenté en toute décontraction assorti de pizzas.</p>
          </div>
